package ca.unb.usermanagement.model;

public class SampleData {
    private int id;
    private String name;

    public int getId() {
        return id;
    }

    public SampleData setId(int id) {
        this.id = id;
        return this;
    }

    public String getName() {
        return name;
    }

    public SampleData setName(String name) {
        this.name = name;
        return this;
    }
}
